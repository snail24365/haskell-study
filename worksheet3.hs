factorial 0 = 1
factorial n = n * factorial (n-1)

productC :: [Int] -> Int
productC [] = 1
productC (x:xs) = x * productC xs

lengthC :: [a] -> Int
lengthC [] = 0
lengthC (x:xs) = 1+ lengthC xs

reverseC :: [a] -> [a]
reverseC [] = []
reverseC (x:xs) = reverse(xs) ++ x


